# Python supports several built-in data types:
# Integers (int): Whole numbers (eg, 10, -5).
# Floats (float): Decimal numbers (eg, 3.14, -0.001).
# Strings (str): Text data enclosed in quotes (eg, "Hello", 'World').
# Booleans (bool): True or False values.
# Lists (list): Ordered collections of items (eg, [1, 2, 3], ["apple", "banana"]).
# Tuples (tuple): Immutable ordered collections (eg, (1, 2, 3), ("apple", "banana")).
# Dictionaries (dict): Key-value pairs (eg, {"name": "Zakir", "age": 34}).
# Sets (set): Unordered collections of unique items (eg, {1, 2, 3}, {"apple", "banana"}).

age = 34  # integer
print (age)
print(type(age))

cgpa = 8.2 # float
print(cgpa)
print(type(cgpa))

name = "Zakir" # string
print(name)
print(type(name))

is_completed = True # can also be False
print(is_completed)
print(type(is_completed))