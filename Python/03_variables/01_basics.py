# Rules of defining a variable in Python
# Variable name must start with a letter (a-z, A-Z) or an underscore (_).
# They can only contain letters, numbers (0-9), and underscores (_).
# Variable names are case-sensitive (age, Age, and AGE are different variables).
# Avoid using Python keywords (like if, else, while, etc.) as variable names.

